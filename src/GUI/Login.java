    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        String user = this.txt_usuario.getText();
        String contrasena = this.txt_contrasena.getText();
        BaseDatos baseDatos = factory.baseDatos();
        boolean aprovada = baseDatos.ValidarLogin(user, contrasena);

        if (aprovada == true) {
            System.out.print("Ha sido validado el inicio de sesi칩n");
            Principal principal = factory.principal();
            principal.setVisible(true);
            this.dispose();
        } else {
            System.out.print(aprovada + "No se ha sido validado el inicio de sesi칩n");
        }
    }//GEN-LAST:event_btn_aceptarActionPerformed

